    private void resetTable() {
        DefaultTableModel model = (DefaultTableModel) tableStudent.getModel();
        model.setRowCount(0);
    }

    private void show_student() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NISN");
        model.addColumn("Nama");
        model.addColumn("Kelas");
        model.addColumn("Alamat");
        model.addColumn("JK");
        model.addColumn("Agama");
        model.addColumn("T.Lahir");
        model.addColumn("Tg.Lahir");
        model.addColumn("P.Ortu");
        try {
            String sql = null;
            if (cbSearch.getModel().getSelectedItem().equals("NISN")) {
                sql = "SELECT * FROM siswa WHERE NISN LIKE '" + txtSearch.getText() + "%' ORDER BY siswa.NISN";
            }else if (cbSearch.getModel().getSelectedItem().equals("Nama")) {
                sql = "SELECT * FROM siswa WHERE Nama LIKE '" + txtSearch.getText() + "%' ORDER BY siswa.Nama";
            }
            java.sql.Connection conn = (Connection) db_connection.configDB();
            Statement st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("NISN"), rs.getString("Nama"), rs.getString("Kelas"), rs.getString("alamat"), rs.getString("jk"), rs.getString("agama"), rs.getString("tmp_lahir"), rs.getDate("tgl_lahir"), rs.getString("pek_ortu")});
            }
            tableStudent.setModel(model);
            String sql_count = "SELECT COUNT(*) AS Jumlah FROM siswa";
            ResultSet rs2 = st.executeQuery(sql_count);
            if(rs2.next()){
                String data_count = rs2.getString("Jumlah");
                lblCountData.setText("" + data_count);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }


