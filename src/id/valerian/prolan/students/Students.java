    private void tableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableStudentMouseClicked
        btnAdd.setEnabled(false);
        btnEdit.setEnabled(true);
        btnDelete.setEnabled(true);
        int i = tableStudent.getSelectedRow();
        TableModel model = tableStudent.getModel();
        txtNISN.setEnabled(false);
        txtNISN.setText(model.getValueAt(i, 0).toString());
        txtNama.setText(model.getValueAt(i, 1).toString());
        txtTK.getModel().setSelectedItem(model.getValueAt(i,2).toString());
        txtAlamat.setText(model.getValueAt(i, 3).toString());
        switch (model.getValueAt(i, 4).toString()) {
            case "L":
                RB1.setSelected(true);
                break;
            case "P":
                RB2.setSelected(true);
                break;
        }
        cbAgama.getModel().setSelectedItem(model.getValueAt(i, 5).toString());
        txtTempatLahir.setText(model.getValueAt(i, 6).toString());
        txtTanggalLahir.setDate(java.sql.Date.valueOf(model.getValueAt(i, 7).toString()));
        txtPO.setText(model.getValueAt(i, 8).toString());
    }//GEN-LAST:event_tableStudentMouseClicked

    private void btnCetakLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakLaporanActionPerformed
        try {
            HashMap parameter = new HashMap();
            parameter.put("jurusan", "");
            parameter.put("kelas", "");
            Connection conn = db_connection.configDB();
            File file = new File("src/id/valerian/prolan/report/reportStudents.jrxml");
            JasperDesign jasperDesign = JRXmlLoader.load(file);
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameter, conn);
            JasperViewer.viewReport(jasperPrint, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCetakLaporanActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        invalid = checkField();
        if (!invalid) {
            try {
                String sql = "INSERT INTO siswa VALUES('" +txtNISN.getText() + "','" + txtNama.getText() + "','" + txtTK.getModel().getSelectedItem().toString() + "','" + txtAlamat.getText() + "','" + checkGender() + "','" + cbAgama.getModel().getSelectedItem().toString() + "','" +txtTempatLahir.getText() + "','" +checkBirthDate() + "','" +txtPO.getText() + "')";
                java.sql.Connection conn = (Connection) db_connection.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Penyimpanan data siswa berhasil");
                resetField();
                resetTable();
                show_student();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

