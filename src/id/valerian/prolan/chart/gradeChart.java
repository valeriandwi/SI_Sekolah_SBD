    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Save");
        chooser.setAcceptAllFileFilterUsed(false);
        FileFilter filter = new FileNameExtensionFilter("PNG", "png");
        chooser.setFileFilter(filter);
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.showSaveDialog(this);
        File file = chooser.getSelectedFile();
        if (file != null) {
            File pngFile = new File(file.toString() + ".png");
            String file_name = chooser.getSelectedFile().toString();
            if (file.exists()) {
                int result = JOptionPane.showConfirmDialog(this,
                        String.format("%s already exists.%n Overwrite?", file.getName()),
                        "File already exists", JOptionPane.YES_NO_OPTION);
                if (result == JOptionPane.YES_OPTION) {
                    save_graph(file_name);
                }
            }else if(pngFile.exists()){
                int result = JOptionPane.showConfirmDialog(this,
                        String.format("%s.png already exists.%n Overwrite?", file.getName()),
                        "File already exists", JOptionPane.YES_NO_OPTION);
                if (result == JOptionPane.YES_OPTION) {
                    save_graph(file_name);
                }
            }else {
                save_graph(file_name);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void save_graph(String file_name) {
        try {
            if (!file_name.toLowerCase().endsWith(".png")) {
                ChartUtilities.saveChartAsPNG(new File(file_name + ".png"), chart, 1024, 768);
            } else if (file_name.toLowerCase().endsWith(".png")) {
                ChartUtilities.saveChartAsPNG(new File(file_name), chart, 1024, 768);
            }
        } catch (Exception ex) {
            Logger.getLogger(studentChart.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

